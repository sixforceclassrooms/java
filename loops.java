
public class loops {

	public static void main(String[] args) {

		int i=0,j=0,k=0;
		for(i=0;i<10;i++)                   //for loop
		{
			System.out.println("number is "+i);
		}
	
		while(j<10)                         //while loop
		{
			System.out.println(j);
			j++;
		}
	
		do                                  //do while loop
		{
			System.out.println("value of k="+k);
			k++;
		}while(k==0);
	}

}
